# Literals
# Ruby'de değişmezler, sabit değerleri doğrudan kod içinde göstermek için kullanılan gösterimdir. Bu değerler sayılar, dizgiler, semboller, diziler, hashler, düzenli ifadeler veya boolean değerler olabilir:

=begin
"Hello World"
375
3.14568
true
false
{ 'a' => 1, 'b' => 2 }
[1, 2, 3]
:sym
nil
=end

# Değişmezlerin kullanılması, ek sözdizimi veya işlemlere gerek kalmadan amaçlanan değerleri doğrudan temsil ettikleri için kodu daha okunabilir ve özlü hale getirir.
